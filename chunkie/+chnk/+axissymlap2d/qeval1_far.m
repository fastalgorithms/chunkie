        coefs = ...
       [0.55536036726979578088E0,                    
        0,                    
        0.26032517215771677229E0,                    
        0,                    
        0.17083839422850163181E0,                    
        0,                    
        0.12723901236810277786E0,                    
        0,                    
        0.10139358798083190111E0];  


        t = 1./x;
        t0 = 1./sqrt(x).^3;
